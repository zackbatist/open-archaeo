# Utility functions for open-archaeo

cnotna <- function(...) {
  x <- c(...)
  x[!is.na(x)]
}

clean_slug <- function(x) {
  x %>% 
    tolower() %>% 
    str_remove("\\.r$") %>% 
    str_replace_all(" ", "-") %>% 
    str_replace_all("_", "-") %>% 
    str_replace_all(coll("."), "-") %>% 
    str_replace_all(coll("/"), "-") %>% 
    str_replace_all("--+", "-")
}

